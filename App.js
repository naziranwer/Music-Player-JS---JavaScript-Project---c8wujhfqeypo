const searchInput = document.getElementById("search-input");

// Get the search results container element
const searchResultsContainer = document.querySelector(".search-result");

// Function to filter songs based on search query
function searchSongs() {
  // Clear the previous search results
  searchResultsContainer.innerHTML = "";

  let query=searchInput.value;

  if(query.trim() === ''){
    return;
  }

  // Filter the songs based on the query
  const filteredSongs = songs.filter(song => {
    const titleMatches = song.songName.toLowerCase().includes(query.toLowerCase());
    return titleMatches;
    // console.log(titleMatches);
  });

  // Display the search results
  console.log(filteredSongs);

  filteredSongs.forEach(song => {
    const resultItem = document.createElement("div");
    resultItem.classList.add("songItem");

    resultItem.innerHTML = `
      <img src="${song.coverPath}" alt="${song.songName}" />
      <span class="songName">${song.songName}</span>
      <span class="songlistplay"><span class="timestamp">05:34<i class="far songItemPlay fa-play-circle"></i></span></span>
    `;
    searchResultsContainer.appendChild(resultItem);
  });
}

